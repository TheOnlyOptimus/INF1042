# Ce programme calcule le nombre moyen de jours par mois
# pour une année normale (365 jours) et une année bissextile (366 jours).

jours_normale = 365 / 12
jours_bissextile = 366 / 12

print("Année non-bissextile :", jours_normale, "jours par mois")
print("Année bissextile :", jours_bissextile, "jours par mois")